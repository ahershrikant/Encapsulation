package encapsulInfo;

public class customerInfo {
	private int acno,mno;
	private String name;
	
	
	public int getAcno() {
		return acno;
	}
	
	public int getMno() {
		return mno;
	}
	
	public String getName() {
		return name;
	}
	
	//setter
	public void setAcno(int acno) {
		this.acno=acno;
	}
	
	public void setMno(int mno) {
		this.mno=mno;
	}
	
	public void setName(String name) {
		this.name=name;
	}

}
